
package tarea;


public class Numeros_Pares {
    public static void main(String[] args){
       for(int i=2; i<101; i+=2){
           if (i == 28){
               System.out.println();
           }
           if(i == 52){
               System.out.println();
           }
           if(i == 76){
               System.out.println();
           }
           System.out.print(i + ", ");
       } 
    }
}
